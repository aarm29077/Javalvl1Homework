package main.com.aca.homework.week1.homework2;

public class TenthQuestion {
    public static void main(String[] args) {
        System.out.print("The first 10 natural numbers are: ");
        for (int i = 1; i <=10 ; i++) {
            if (i !=10){
                System.out.print(i+ ", ");
            }
            else{
                System.out.print(i);
            }
        }
    }
}
